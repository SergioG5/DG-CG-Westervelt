function [diam] = getDiameter(T)
% ------------------------------------------------------------------------------
% Get diameter of a 1D-simplex.
% OUTPUT:
%   diam: diameter of the 1D-simplex, that is maximum edge length.
% ------------------------------------------------------------------------------
	diam = T.d_;
end


